end
     
function GUI.init()
GUI.time = GAMEPLAY.GET_GAME_TIMER()

	for i, coords in pairs(stuntJumps) do
		blip[i] = UI.ADD_BLIP_FOR_COORD(coords[1],coords[2],coords[3])
